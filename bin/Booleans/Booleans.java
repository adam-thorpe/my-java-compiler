public class Booleans {

  public boolean True() {
    boolean output = true;
    return output;
  }

  public boolean False() {
    return false;
  }

  public boolean TestAND() {
    return true && false;
  }

  public boolean TestOR() {
    return false || true;
  }

  public boolean TestAND2(boolean a, boolean b) {
    return a && b;
  }

  public boolean TestOR2(boolean a, boolean b) {
    return a || b;
  }
}
